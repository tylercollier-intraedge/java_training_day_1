/**
 * Rearrange the code snippets to make a working Java program that produces the output:
 * "a-b c-d"
 */
public class Jumble {
	public static void main(String[] args) {
		// SNIPPET 2
		int x = 3;

		// SNIPPET 5
		while(x>0) {

			// SNIPPET 4
			if(x>2) {
				System.out.print("a");
			}



//			 SNIPPET 3
			if(x==1) {
				System.out.print("d");
				x = x-1;
			}



//			SNIPPET 6
			if(x==2) {
				System.out.print("b c");
			}

			// SNIPPET 1
			x = x-1;
			System.out.print("-");
		}

    }
}